package tddc17;

public final class Point2D {
	private int xpos;
	private int ypos;
	/**
	 * @param xpos
	 * @param ypos
	 */
	public Point2D(int xpos, int ypos) {
		super();
		this.xpos = xpos;
		this.ypos = ypos;
	}
	
	public int getXpos() {
		return xpos;
	}

	public int getYpos() {
		return ypos;
	}
	
}
